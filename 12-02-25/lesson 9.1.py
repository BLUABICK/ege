# Экранированные последовательности позволяют использовать символы,
# недоступные для ввода с клавиатуры.
#
#  Также можно экранировать символы ', " и \ внутри строк.

# \n - переход на новую строку
print('C:\\privet\nig')

# Экранирование слэша для вывода всего текста на экран
print('C:\\privet\\nig')

#Сырые строки - r'', отключают выполнение спец символов
print(r'C:\\privet\nig')

#len служит для подсчёта количества элементов последовательности

#capitalize() - возвращает копию строки

name = 'aRTeM'
